using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class Hover : MonoBehaviour {

    float originalY;

    public float floatStrength = 0.1f;

    private void Start()
    {
       this.originalY = this.transform.position.y + 0.2f;
    }

    private void Update()
    {
        this.transform.position = new Vector3(this.transform.position.x, originalY + ((float)Mathf.Sin(Time.time) * floatStrength), this.transform.position.z);
    }
}